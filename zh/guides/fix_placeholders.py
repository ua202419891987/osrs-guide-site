def process_file(filepath):
    """Process a single HTML file, replacing placeholders with real Chinese."""
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    original = content
    count = 0
    
    # Process TOC links (中文翻译)
    for en, cn in TOC_MAP.items():
        old = f'{en}（中文翻译）'
        new = f'{en}（{cn}）'
        if old in content:
            content = content.replace(old, new)
            count += 1
    
    # Process H2 (中文标题)
    for en, cn in H2_MAP.items():
        old = f'{en}（中文标题）'
        new = f'{en}（{cn}）'
        if old in content:
            content = content.replace(old, new)
            count += 1
    
    # Process H3 (中文说明)
    for en, cn in H3_MAP.items():
        old = f'{en}（中文说明）'
        new = f'{en}（{cn}）'
        if old in content:
            content = content.replace(old, new)
            count += 1
    
    if count > 0:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
    
    return count, (original != content)

